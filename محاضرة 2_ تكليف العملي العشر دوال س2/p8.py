import pandas as pd

# 1) إنشاء DataFrame مع عمود تواريخ
df = pd.DataFrame({
    'D': ['2025-01-01', '2025-01-02', '2025-01-03', '2025-01-04', '2025-01-05'],
    'A': [1, 2, 3, 4, 5],
    'B': [10, 20, 30, 40, 50]
})

# 2) تحويل العمود D إلى نوع datetime
df['D'] = pd.to_datetime(df['D'])

# 3) جعله فهرسًا ثم إعادة التحجيم كل يومين (2D) مع جمع القيم الرقمية
resampled = (
    df
    .set_index('D')          # جعل D هو الفهرس الزمني
    .resample('2D')          # كل يومين
    .sum(numeric_only=True)  # جمع الأعمدة الرقمية فقط
)

# 4) طباعة أو حفظ النتيجة
print(resampled)
